#OPERADORES RELACIONALES
"""
> mayor que
< menor que
>= mayor o igual que
<= menor o igal que
!= diferente
== igual
"""
#En este tipo de comparaciones se asigna valor booleano (true or false)
#MENOR QUE
resultado = 10<20
print("10 < 20: ", resultado)

#MAYOR QUE
resultado = 10>20
print("10 > 20: ",resultado)

#IGUAL QUE
resultado = 10==20
print("10 ==20", resultado)

#DIFERENTE QUE
resultado = 10!=20
print("10!=20", resultado)

#MENOR O IGUAL QUE
resultado = 10<=20
print("10<=20", resultado)

#MAYOR O IGUAL QUE
resultado = 10>=20
print("10>=20", resultado)

