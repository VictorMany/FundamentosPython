#Suma
num1 = 10
num2 = 3.9
resultado = num1+num2
print("La suma: ", resultado)
#Resta
num1 = 10
num2 = 3.9
resultado = num1-num2
print("La resta: ", resultado)
#DIVISION
num1 = 10
num2 = 3.9
resultado = num1/num2
print("La division: ", resultado)
#Si le ponemos doble slash nos da como resultado un numero entero
num1 = 10
num2 = 3.9
resultado = num1//num2
print("La divison entera: ", resultado)
#Modulo o residuo
num1 = 10
num2 = 3
resultado = num1%num2
print("El residuo: ", resultado)
#Exponenciacion
num1 = 2
potencia = 5
resultado = num1**potencia
print("La potencia: ", resultado)
