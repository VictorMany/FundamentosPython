#ENTRADAS
#El input guarda datos de tipo cadena

"""STRINGS"""
nombre = input("Ingresa tu nombre: ")
print(nombre)

"""INT"""
numero = int(input("Ingresa tu numero: "))
print(numero)

"""FLOAT"""
numeroFloat = float(input("Ingresa tu numero: "))
print(f"tu numero es:{numeroFloat}")

"""BOOLEAN"""
myboolean = bool(input("Ingresa tu true or false: "))
print(f"tu true or false es:{myboolean}")