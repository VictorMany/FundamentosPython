#Si queremos converir de cadena a entero
n = int("10")
print(n)

#cadena a float
n = float("10.8989")
print(n)


#entero o float a string
n = str(10.8989)
print(n)

#Numero abinario

n = bin(10)
print(n)

#Numero a hexadecimal

n = hex(100)
print(n)


#Hexadecimal a entero
"""Le debemos de especificar el numero en comillas y tambien la base en la que este se encuetra"""

n = int("64", 16)
print(n)

#Binario a un etero

n = int("0b1010", 2)
print(n)

#Valor absoluto

n = abs(-10.8989)
print(n)

#Redondear un nuemero
n = round(10.8989)
print(n)

#Conteo de caracteres
n = len("10.8989")
print(n)




